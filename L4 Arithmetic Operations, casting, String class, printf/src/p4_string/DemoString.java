package p4_string;

import com.sun.jdi.Location;

public class DemoString {

	public static void main(String[] args) {
		// primitive data type/ variable, where value ( 5 or 10) is stored at the named memory location
		int n = 5;
		n = 10;
		
		
		// Reference data type / variable, where the address of the value ("Joey") is stored at the
		// named memory location
		String name = "Joey";
		
		
		double number = 3.5;
		
		String s = "123";
		
		String str = "Hi";
		// "Hi" is stored at some unnamed memory Location. That memory location is now stored at
		// a memory location called str.
	}

}
